// ============================================================

void main(List<String> args) async {
  final parser = ArgParser()
    ..addCommand('create-plugin')
    ..addCommand('list-plugins')
    ..addCommand('inspect')
    ..addFlag('help', abbr: 'h', negatable: false);

  final results = parser.parse(args);

  if (results['help'] as bool || results.command == null) {
    _printHelp();
    return;
  }

  switch (results.command!.name) {
    case 'create-plugin':
      await _createPlugin(results.command!);
      break;
    case 'list-plugins':
      await _listPlugins();
      break;
    default:
      _printHelp();
  }
}

Future<void> _createPlugin(ArgResults args) async {
  stdout.write('Plugin name: ');
  final name = stdin.readLineSync()!.trim();
  
  stdout.write('Version (1.0.0): ');
  final version = stdin.readLineSync()!.trim().isEmpty 
      ? '1.0.0' 
      : stdin.readLineSync()!.trim();
  
  stdout.write('Description: ');
  final description = stdin.readLineSync()!.trim();

  final pluginDir = 'plugins/$name';
  
  print('\n📦 Creating plugin: $name@$version');

  // ساختار پوشه
  final dirs = [
    '$pluginDir/lib',
    '$pluginDir/android/src/main/kotlin',
    '$pluginDir/ios/Classes',
    '$pluginDir/js',
  ];

  for (final dir in dirs) {
    await Directory(dir).create(recursive: true);
  }

  // manifest.json
  await File('$pluginDir/manifest.json').writeAsString(
    _generateManifest(name, version, description),
  );

  // Dart implementation
  await File('$pluginDir/lib/${name}_plugin.dart').writeAsString(
    _generateDartPlugin(name, version, description),
  );

  // JS wrapper
  await File('$pluginDir/js/${name}.js').writeAsString(
    _generateJsWrapper(name),
  );

  // pubspec.yaml
  await File('$pluginDir/pubspec.yaml').writeAsString(
    _generatePubspec(name, description),
  );

  print('✅ Plugin created at: $pluginDir');
  print('');
  print('Next steps:');
  print('  1. Edit plugins/$name/lib/${name}_plugin.dart');
  print('  2. Add your methods to supportedMethods');
  print('  3. Register in lib/di/service_locator.dart');
}

String _generateManifest(
  String name,
  String version,
  String description,
) => '''{
  "name": "$name",
  "version": "$version",
  "description": "$description",
  "methods": ["exampleMethod"],
  "permissions": [],
  "capabilities": {
    "supportsStreaming": false,
    "supportsBatch": true,
    "supportsCache": false,
    "maxConcurrentCalls": 10
  }
}
''';

String _generateDartPlugin(
  String name,
  String version,
  String description,
) {
  final className = _toPascalCase(name);
  
  return '''import 'dart:async';
import '../../../packages/plugin_engine/lib/src/plugin_interface.dart';

/// $description
class ${className}Plugin extends Plugin {
  @override
  String get name => '$name';

  @override
  String get version => '$version';

  @override
  String get description => '$description';

  @override
  List<String> get supportedMethods => [
    'exampleMethod',
  ];

  @override
  List<String> get requiredPermissions => [];

  @override
  Future<void> onInitialize() async {
    // Initialize resources
  }

  @override
  Future<void> onDispose() async {
    // Cleanup resources
  }

  @override
  Future<dynamic> onCall(
    String method,
    Map<String, dynamic> args,
  ) async {
    switch (method) {
      case 'exampleMethod':
        return _exampleMethod(args);
      default:
        throw UnsupportedError('Method "\$method" not supported');
    }
  }

  Future<Map<String, dynamic>> _exampleMethod(
    Map<String, dynamic> args,
  ) async {
    // TODO: Implement your method
    return {
      'result': 'Hello from $name plugin!',
      'args': args,
    };
  }
}
''';
}

String _generateJsWrapper(String name) => '''/**
 * $name Plugin JS Wrapper
 * Auto-generated by Bridge CLI
 */

const ${_toCamelCase(name)} = {
  async exampleMethod(args = {}) {
    return Native.call({
      plugin: '$name',
      method: 'exampleMethod',
      args
    });
  }
};

// Export
if (typeof module !== 'undefined') {
  module.exports = ${_toCamelCase(name)};
}
''';

String _generatePubspec(String name, String description) => '''name: ${name}_plugin
description: $description
version: 1.0.0

environment:
  sdk: '>=3.0.0 <4.0.0'
  flutter: ">=3.0.0"

dependencies:
  flutter:
    sdk: flutter
''';

String _toPascalCase(String s) {
  return s.split('_').map((w) => 
    w[0].toUpperCase() + w.substring(1)
  ).join('');
}

String _toCamelCase(String s) {
  final pascal = _toPascalCase(s);
  return pascal[0].toLowerCase() + pascal.substring(1);
}

Future<void> _listPlugins() async {
  final pluginsDir = Directory('plugins');
  if (!await pluginsDir.exists()) {
    print('No plugins directory found');
    return;
  }

  print('\n📦 Registered Plugins:\n');
  
  await for (final entity in pluginsDir.list()) {
    if (entity is Directory) {
      final manifestFile = File('${entity.path}/manifest.json');
      if (await manifestFile.exists()) {
        final content = await manifestFile.readAsString();
        print('  ✓ ${entity.path.split('/').last}');
      }
    }
  }
}

